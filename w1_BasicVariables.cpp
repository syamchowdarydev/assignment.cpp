#include <iostream>
using namespace std;

int main() {
    int age = 20;
    float height = 5.9;
    char grade = 'A';

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;

    return 0;
}

/*
Sample Output:
Age: 20
Height: 5.9
Grade: A
*/
